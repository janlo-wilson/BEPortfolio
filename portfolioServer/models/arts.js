const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const artsSchema = new Schema({
    name: {
        type: String,
        required: true,
        unique: true
    },
    description: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
    elevation: {
        type: Number,
        required: true
    },
    cost: {
        type: Currency,
        required: true,
        min: 0
    },
    featured: {
        type: Boolean,
        default: false
    },
}, {
    timestamps: true
});

const Arts = mongoose.model('Event', artsSchema);

module.exports = Arts;