const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const sportSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    image: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      required: true,
    },
    time: {
      type: String,
      required: true,
    },
    fragment: {
      type: String,
      required: true,
    },
    url: {
      type: String,
      required: true,
    },
    featured: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);

const Sport = mongoose.model("Sport", sportSchema);

module.exports = Sport;
